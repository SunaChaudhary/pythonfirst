#operators in python

num1=10
num2=3

#basic operators
print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1//num2) #gives integer division
print(num1%num2) #gives remainder
print(num1**num2) #num1^num2


#comparision operators
print(num1==num2)
print(num1!=num2)
print(num1>num2)
print(num1<num2)
print(num1>=num2)
print(num1<=num2)
