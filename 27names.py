#finding total sum and average using for loop
number=[20,10,30]
total=0
n=0
for i in number:
    total+=i #finding total like total=total+i
    n+=1 #increamenting thi divider by 1 each time like n=n+1
    average=total/(n) #average=total divide by total number of elements
print(f"Average is {average}")
print(f"total sum is {total}")






